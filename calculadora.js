class Calculadora {
    sumar(num1, num2) {
        return num1 + num2;
    }


    //resta

    restar(num1, num2) {
        return num1 - num2;
    }
    
    //multiplicacion

    multiplicar(num1, num2) {
        return num1 * num2;
    }

    //division

    dividir(num1, num2) {
        return num1 / num2;
    }


} 